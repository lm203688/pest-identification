def call_qwen(prompt, retries=3):
    """调用通义千问API"""
    for attempt in range(retries):
        try:
            resp = requests.post(
                API_URL,
                headers={
                    'Authorization': f'Bearer {API_KEY}',
                    'Content-Type': 'application/json',
                },
                json={
                    'model': 'qwen-turbo',
                    'messages': [{'role': 'user', 'content': prompt}],
                    'temperature': 0.7,
                    'max_tokens': 2000,
                },
                timeout=30,
            )
            if resp.status_code == 200:
                data = resp.json()
                return data['choices'][0]['message']['content']
            elif resp.status_code == 429:
                time.sleep(5)
                continue
            else:
                return None
        except Exception as e:
            if attempt < retries - 1:
                time.sleep(3)
            else:
                return None
    return None


def generate_single(crop, pest_name):
    """生成单条病虫害知识"""
    prompt = f"""请提供关于农作物病虫害"{pest_name}"（危害{crop}）的专业知识。

严格按照以下JSON格式输出，不要输出任何其他内容：
{{"name":"{pest_name}","aliases":["别名1","别名2"],"type":"病害或虫害","host_crops":["{crop}"],"summary":"一句话简介","symptoms":"症状描述200字以上","conditions":"发病条件或发生规律150字以上","prevention":"综合防治方案200字以上","medicine":"推荐用药列出3-5种具体农药名称和用法"}}"""

    content = call_qwen(prompt)
    if not content:
        return None

    # 提取JSON
    json_match = re.search(r'\{[\s\S]*\}', content)
    if not json_match:
        return None

    try:
        data = json.loads(json_match.group())
        # 验证必要字段
        if not data.get('name') or not data.get('symptoms'):
            return None
        # 标准化type
        if '虫' in data.get('type', ''):
            data['type'] = '虫害'
        else:
            data['type'] = '病害'
        data['source'] = 'AI生成'
        data['source_url'] = ''
        return data
    except json.JSONDecodeError:
        return None


def main():
    output = DATA_DIR / "pest_knowledge_ai.json"

    # 加载已有数据
    with open(output, 'r', encoding='utf-8') as f:
        existing = json.load(f)

    existing_names = {r['name'] for r in existing}
    print(f"已有: {len(existing)} 条")

    # 收集待新增
    to_add = []
    for crop, pests in EXPAND_PESTS.items():
        for pest in pests:
            if pest not in existing_names:
                to_add.append((crop, pest))

    print(f"待新增: {len(to_add)} 条")
    print(f"目标总计: {len(existing) + len(to_add)} 条\n")

    success = 0
    fail = 0

    for i, (crop, pest_name) in enumerate(to_add, 1):
        print(f"  [{i}/{len(to_add)}] {pest_name}...", end=" ", flush=True)

        record = generate_single(crop, pest_name)

        if record:
            existing.append(record)
            existing_names.add(record['name'])
            success += 1
            s = "✓" if record.get('symptoms') else "✗"
            p = "✓" if record.get('prevention') else "✗"
            m = "✓" if record.get('medicine') else "✗"
            print(f"OK 症状:{s} 防治:{p} 用药:{m}")

            # 每50条保存一次
            if success % 50 == 0:
                with open(output, 'w', encoding='utf-8') as f:
                    json.dump(existing, f, ensure_ascii=False, indent=2)
        else:
            fail += 1
            print("FAIL")

        time.sleep(0.3)

    # 最终保存
    with open(output, 'w', encoding='utf-8') as f:
        json.dump(existing, f, ensure_ascii=False, indent=2)

    # 统计
    disease_count = sum(1 for r in existing if '虫' in r.get('type', ''))
    pest_count = len(existing) - disease_count

    print(f"\n{'='*50}")
    print(f"完成！共 {len(existing)} 条（新增 {success}，失败 {fail}）")
    print(f"  病害: {pest_count}")
    print(f"  虫害: {disease_count}")

    crop_stats = {}
    for r in existing:
        for c in r.get('host_crops', []):
            if isinstance(c, str):
                crop_stats[c] = crop_stats.get(c, 0) + 1
    print(f"\n覆盖 {len(crop_stats)} 种作物:")
    for crop, cnt in sorted(crop_stats.items(), key=lambda x: -x[1])[:20]:
        print(f"  {crop}: {cnt}")

    print(f"\n保存: {output}")


if __name__ == "__main__":
    main()
