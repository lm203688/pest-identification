def generate_pest_knowledge(crop, pest_name):
    prompt = f'提供农作物病虫害"{pest_name}"（危害{crop}）的专业知识，严格JSON格式：name/aliases(别名数组)/type(病害或虫害)/host_crops(作物数组)/summary(50字简介)/symptoms(200字症状)/conditions(150字发病条件)/prevention(200字防治方案)/medicine(3-5种具体农药名称和用量)'

    try:
        result = subprocess.run(
            ['z-ai', 'chat', '-p', prompt, '-m', 'glm-4-flash'],
            capture_output=True, text=True, timeout=45
        )
        raw = result.stdout.strip()
        start = raw.find('{')
        if start < 0:
            return None

        outer = json.loads(raw[start:])
        content = outer['choices'][0]['message']['content']
        json_match = re.search(r'\{[\s\S]*\}', content)
        if not json_match:
            return None

        data = json.loads(json_match.group())
        data['source'] = 'AI生成'
        data['source_url'] = ''
        return data

    except Exception as e:
        return None


def main():
    # 加载已有数据
    output = DATA_DIR / "pest_knowledge_ai.json"
    existing = []
    existing_names = set()

    if output.exists():
        with open(output, 'r', encoding='utf-8') as f:
            existing = json.load(f)
        existing_names = {r['name'] for r in existing}
        print(f"加载已有 {len(existing)} 条，跳过已生成的")

    # 统计待生成数量
    total_new = 0
    for crop, pests in PEST_LIST.items():
        for pest in pests:
            if pest not in existing_names:
                total_new += 1

    print(f"待新增: {total_new} 条")
    print(f"目标总计: {len(existing) + total_new} 条")

    count = 0
    success = 0
    fail = 0

    for crop, pests in PEST_LIST.items():
        new_in_crop = [p for p in pests if p not in existing_names]
        if not new_in_crop:
            continue

        print(f"\n{'='*40}")
        print(f"[{crop}] 新增 {len(new_in_crop)} 种")
        print(f"{'='*40}")

        for pest in new_in_crop:
            count += 1
            print(f"  [{count}/{total_new}] {pest}...", end=" ", flush=True)

            record = generate_pest_knowledge(crop, pest)

            if record:
                existing.append(record)
                existing_names.add(pest)
                success += 1
                s = "✓" if record.get('symptoms') else "✗"
                p = "✓" if record.get('prevention') else "✗"
                m = "✓" if record.get('medicine') else "✗"
                print(f"OK 症状:{s} 防治:{p} 用药:{m}")

                # 每10条保存一次
                if success % 10 == 0:
                    with open(output, 'w', encoding='utf-8') as f:
                        json.dump(existing, f, ensure_ascii=False, indent=2)
            else:
                fail += 1
                print("FAIL")

            time.sleep(0.3)

    # 最终保存
    with open(output, 'w', encoding='utf-8') as f:
        json.dump(existing, f, ensure_ascii=False, indent=2)

    # 统计
    disease_count = sum(1 for r in existing if '虫' in r.get('type', ''))
    pest_count = len(existing) - disease_count

    print(f"\n{'='*50}")
    print(f"生成完成！共 {len(existing)} 条（新增 {success}，失败 {fail}）")
    print(f"  病害: {pest_count}")
    print(f"  虫害: {disease_count}")
    print(f"  有症状: {sum(1 for r in existing if r.get('symptoms'))}")
    print(f"  有防治: {sum(1 for r in existing if r.get('prevention'))}")
    print(f"  有用药: {sum(1 for r in existing if r.get('medicine'))}")

    # 按作物统计
    crop_stats = {}
    for r in existing:
        for c in r.get('host_crops', []):
            crop_stats[c] = crop_stats.get(c, 0) + 1
    print(f"\n按作物分布（{len(crop_stats)} 种作物）:")
    for crop, cnt in sorted(crop_stats.items(), key=lambda x: -x[1]):
        print(f"  {crop}: {cnt}")

    print(f"\n保存: {output}")
    return existing


